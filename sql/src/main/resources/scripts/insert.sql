INSERT INTO USERS (ID, NAME, SURNAME, BIRTHDATE)
SELECT
    LEVEL,
    dbms_random.string ('U', 4),
    dbms_random.string ('U', 4),
    TO_DATE(TRUNC(DBMS_RANDOM.VALUE(TO_CHAR(DATE '1950-01-01','J'),TO_CHAR(DATE '2010-12-31','J'))),'J')
FROM  dual CONNECT BY LEVEL <= 1000;

INSERT INTO FRIENDSHIPS (USER_ID1,USER_ID2,TIMESTAMP)

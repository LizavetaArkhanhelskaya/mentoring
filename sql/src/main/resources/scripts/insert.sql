    TO_DATE(TRUNC(DBMS_RANDOM.VALUE(TO_CHAR(DATE '2015-01-01','J'),TO_CHAR(DATE '2015-12-31','J'))),'J')
FROM  dual CONNECT BY LEVEL <= 70000;

